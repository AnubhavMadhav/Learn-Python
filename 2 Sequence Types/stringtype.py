# Create a String
s= "I am Anubhav"
print(s)

# Multi Line String
s1 = """I am  Anubhav
I am a very good boy.
I am a Computer Science Engineer"""
print(s1)

# Index of a String
print(s[2])

# Print a String 2 times
print(s*2)

# Print a String 3 times
print(s1*3)

# Print the character at index 3 of a String 2 times
print(s[3]*2)

# Length of a String
print(len(s))
print(len(s1))